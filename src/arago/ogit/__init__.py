def create_namespace_instances(gen_iter: Callable[[], Iterable[OntologyNamespace]]) -> Dict[str, OntologyNamespace]:
    r: Dict[str, OntologyNamespace] = {}
    for n in gen_iter():
        k = Prefix(n.prefix).constant
        r[k] = n
    for n in r.values():
        n.finalize(r)
    return r


m: Mapping[str, OntologyNamespace] = create_namespace_instances(namespace_data)


